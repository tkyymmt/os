int read_keyboard()
{
	uint8_t scan_code;
	static bool shift_flag = false;
	int ch = '\0';

	while (!(inb(0x64) & 0x01));

	scan_code = inb(0x60);

	switch (scan_code) {
	case 0x2A:
	case 0x36:
		shift_flag = true;
		break;
	case 0xAA:
	case 0xB6:
		shift_flag = false;
		break;
	default:
		ch = shift_flag ? shift_map[scan_code] : normal_map[scan_code];
		break;
	}

	return ch;
}

void clear_key_buf()
{
	while (inb(0x64) & 0x01)
		inb(0x60);
}

void init_keyboard()
{
	clear_key_buf();
}
